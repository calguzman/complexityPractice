function matrix(m, n) {
  var result = [] // O(1)
  for(let i = 0; i < n; i++) { // (1 Index Increment + 1 Random Number + 1 new Array Assign + 1 Add to Matrix)
      result.push(new Array(m).fill(Math.floor(1000 + Math.random() * 9000)))  // 4N
  }
  return result // O(1)
}
console.table(matrix(3,3))

