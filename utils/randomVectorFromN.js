const randomVectorFromN = (n) => {
 let vector=[];                                   // O(1)
  for (let index = 0; index < n; index++) { 
    // 3N   (1 Index + 1 RandomNumber + 1 Vector Index Assign)
    // Create a random Number with 4 Digits;
    vector[index] =  Math.floor(1000 + Math.random() * 9000) 
  }
  return vector; (1) // O(1)
}
console.log(randomVectorFromN(20));


