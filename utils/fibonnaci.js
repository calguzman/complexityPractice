const fibonacci = (n) => {
  if (n == 1) {  // O(1)
    return 0; // O(1)
  }
  if (n == 2) { // O(1)
    return 1; // O(1)
  }
  return fibonacci(n - 1) + fibonacci(n - 2);
};

console.log(fibonacci(5));



/* -------------------------------------------------------------------------- */
/*                               Time Complexity                              */
/* -------------------------------------------------------------------------- */

// (n-1) + (n-2) + (1) 
// o (n^2)

/* -------------------------------------------------------------------------- */
/*                              Space Complexity                              */
/* -------------------------------------------------------------------------- */

// O(N) 